export function MoreSheet({ onClose }: { onClose: () => void }) {
  const pathname = usePathname();
  const { t } = useLanguage();
  const { user, signOut } = useAuth();
  const { platform } = usePlatform();
  const moreItems = platform === 'youth' ? youthMoreItems : platform === 'women' ? womenMoreItems : menMoreItems;

  // Prevent body scroll while sheet is open
  useEffect(() => {
    document.body.style.overflow = 'hidden';
    return () => {
      document.body.style.overflow = '';
    };
  }, []);

  return (
    <div className="fixed inset-0 z-50 lg:hidden" role="dialog" aria-modal="true">
      {/* Backdrop */}
      <div className="absolute inset-0 bg-black/60 backdrop-blur-sm" onClick={onClose} />

      {/* Sheet */}
      <div
        className="absolute bottom-0 inset-x-0 bg-mgsr-card rounded-t-2xl border-t border-mgsr-border animate-slide-up"
        style={{ paddingBottom: 'env(safe-area-inset-bottom, 0px)' }}
      >
        {/* Drag handle */}
        <div className="flex justify-center pt-3 pb-2">
          <div className="w-10 h-1 rounded-full bg-mgsr-border" />
        </div>

        {/* Title */}
        <div className="px-4 pb-3 flex items-center justify-between">
          <h2 className="text-base font-semibold text-mgsr-text">{t('nav_more') || 'More'}</h2>
          <button
            onClick={onClose}
            className="w-8 h-8 rounded-full bg-mgsr-dark/60 flex items-center justify-center text-mgsr-muted hover:text-mgsr-text transition"
            aria-label="Close"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Grid of nav items */}
        <div className="px-4 pb-4 grid grid-cols-3 gap-1.5 sm:gap-2">
          {moreItems.map((item) => {
            const active = pathname === item.href || pathname.startsWith(item.href + '/');
            const Icon = icons[item.href] || DefaultIcon;
            const isWomen = platform === 'women';
            const isYouth = platform === 'youth';
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={onClose}
                className={`flex flex-col items-center justify-center gap-1 sm:gap-1.5 p-2.5 sm:p-3 rounded-xl min-h-[68px] sm:min-h-[72px] transition ${
                  active
                    ? isYouth
                      ? 'bg-[var(--youth-cyan)]/15 text-[var(--youth-cyan)]'
                      : isWomen
                        ? 'bg-[var(--women-rose)]/15 text-[var(--women-rose)]'
                        : 'bg-[var(--mgsr-accent-dim)] text-[var(--mgsr-accent)]'
                    : 'bg-mgsr-dark/40 text-mgsr-muted hover:text-mgsr-text hover:bg-mgsr-dark/60'
                }`}
              >
                <Icon className="w-5 h-5" />
                <span className="text-[11px] font-medium leading-tight text-center">{t(item.labelKey)}</span>
              </Link>
            );
          })}
        </div>

        {/* User info + sign out */}
        <div className="px-4 pb-4 pt-2 border-t border-mgsr-border flex items-center justify-between">
          <p className="text-xs text-mgsr-muted truncate flex-1">{user?.email}</p>
          <button
            onClick={() => {
              signOut();
              onClose();
            }}
            className="text-xs text-[var(--mgsr-accent)] hover:underline min-h-[44px] flex items-center px-2"
          >
            {t('sign_out')}
          </button>
        </div>
      </div>
    </div>
  );
}
