export function getCountryDisplayName(country: string | null | undefined, isHebrew: boolean): string {
  if (!country?.trim()) return '';
  const trimmed = country.trim();
  if (!isHebrew) return trimmed;
  const key = Object.keys(COUNTRY_EN_TO_HE).find((k) => k === trimmed || k.toLowerCase() === trimmed.toLowerCase());
  return (key ? COUNTRY_EN_TO_HE[key] : trimmed) ?? trimmed;
}
